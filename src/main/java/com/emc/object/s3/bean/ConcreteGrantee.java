package com.emc.object.s3.bean;

/**
 * @author seibed
 *
 */
public class ConcreteGrantee {

    private String id;
    private String type;
    private String uri;

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     * @return the uri
     */
    public String getUri() {
        return uri;
    }

    /**
     * @param uri the uri to set
     */
    public void setUri(String uri) {
        this.uri = uri;
    }

    public AbstractGrantee toAbstractGrantee() {
        AbstractGrantee output = ("Group".equals(type)) ? new Group() : new CanonicalUser();
        if (output instanceof Group) {
            ((Group) output).setUri(getUri());
        } else {
            ((CanonicalUser) output).setId(getId());
        }
        return output;
    }
}
